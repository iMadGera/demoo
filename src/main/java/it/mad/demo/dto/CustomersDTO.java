package it.mad.demo.dto;

public class CustomersDTO extends AbstractDTO<Long> {
    private Long id;
    private String name;
    private String surname;

    public CustomersDTO() {
        super();
    }

    @Override
    public Long getId() {
        return this.id;
    }

    @Override
    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return this.surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }
}